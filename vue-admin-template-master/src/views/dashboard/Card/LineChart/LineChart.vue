<template>
  <div class="charts" ref="charts">
  </div>
</template>

<script>
//引入echarts
import echarts from "echarts";

export default {
  name: 'LineChart',
  mounted(){
    //初始化echarts实例
    let lineCharts = echarts.init(this.$refs.charts);
    //配置数据
    lineCharts.setOption({
      xAxis:{
        show:false,//隐藏x轴
        type:'category'
      },
      yAxis:{
        show:false,
        
      },
      //系列
      series:[
        {
          type:'line',
          data:[10,7,33,12,48,9,29,10,44],
          //拐点的样式设置
          itemsStyle:{
            opacity:0
          },
          //线条的样式
          lineStyle:{
            color:"purple"
          },
          //填充颜色的设置
          areaStyle:{
            color:{
              type:"linear",
              x:0,
              y:0,
              x2:0,
              y2:1,
              colorStops:[
                {
                  offset:0,
                  color:"purple",//0%处颜色
                },
                {
                  offset:1,
                  color:"#fff"//100%处的颜色
                }
              ],
              global:false,//缺省为false
            }
          }
        }
      ],
      //布局模式
      grid:{
        left:0,
        top:0,
        bottom:0,
        right:0
      }
    })
  }
}
</script>

<style scoped>
  .charts{
    width: 100%;
    height:100%;
  }
</style>
